"""Reset state and re-submit all 8 city orders using verified data from previous run."""
import json
import os

import requests
from dotenv import load_dotenv

load_dotenv()

HUB_URL = os.environ["HUB_URL"].rstrip("/")
HUB_API_KEY = os.environ["HUB_API_KEY"]


def call_api(answer: dict) -> dict:
    payload = {"apikey": HUB_API_KEY, "task": "foodwarehouse", "answer": answer}
    print(f"→ {json.dumps(answer, ensure_ascii=False)}")
    resp = requests.post(f"{HUB_URL}/verify", json=payload, timeout=30)
    result = resp.json()
    print(f"← {json.dumps(result, ensure_ascii=False)}")
    return result


def items_list(items_dict: dict) -> list:
    """Convert {name: quantity} to [{"name": name, "items": quantity}]."""
    return [{"name": k, "items": v} for k, v in items_dict.items()]


