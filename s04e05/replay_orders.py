def main():
    # Step 1: reset
    print("\n=== RESET ===")
    call_api({"tool": "reset"})

    # Step 2: create and fill each order sequentially
    for order in ORDERS:
        print(f"\n=== {order['title']} ===")
        result = call_api({
            "tool": "orders",
            "action": "create",
            "title": order["title"],
            "creatorID": order["creatorID"],
            "destination": order["destination"],
            "signature": order["signature"],
        })
        if result.get("code") != 110:
            print(f"  ERROR creating order: {result}")
            return

        order_id = result["order"]["id"]
        append_result = call_api({
            "tool": "orders",
            "action": "append",
            "id": order_id,
            "items": items_list(order["items"]),
        })
        if append_result.get("code") != 120:
            print(f"  ERROR appending items: {append_result}")
            return

    # Step 3: done
    print("\n=== DONE ===")
    call_api({"tool": "done"})


if __name__ == "__main__":
    main()
